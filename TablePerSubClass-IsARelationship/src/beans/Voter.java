package beans;

public class Voter {
	private int voterId;
	private String name;
	private int vage;
	public int getVoterId() {
		return voterId;
	}
	public void setVoterId(int voterId) {
		this.voterId = voterId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getVage() {
		return vage;
	}
	public void setVage(int vage) {
		this.vage = vage;
	}
	
	
}
